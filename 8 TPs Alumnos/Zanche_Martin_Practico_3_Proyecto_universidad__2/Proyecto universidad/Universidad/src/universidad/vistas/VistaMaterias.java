    private void btnGuardarMatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarMatActionPerformed
        // TODO add your handling code here:
         
         if((tbNombreMat.getText().isEmpty()) 
             || tbPeriodoMat.getText().isEmpty() || tbResponsableMat.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Ingrese datos", "Info", JOptionPane.WARNING_MESSAGE);  
         }else{
        nombre= tbNombreMat.getText();
        responsable=tbResponsableMat.getText();
        periodo=tbPeriodoMat.getText();
        Materia materia = new Materia(nombre,responsable,periodo);
          try {
               md.guardarMateria(materia);
           } catch (SQLException ex) {
           Logger.getLogger(VistaMaterias.class.getName()).log(Level.SEVERE, null, ex);
           }
        tbIdMat.setText(materia.getId()+"");
         }
          JOptionPane.showMessageDialog(null, "Se guardo materia", "Info", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btnGuardarMatActionPerformed

