    private void btnBuscarMatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarMatActionPerformed
        // TODO add your handling code here:
        
        int id;
        String nombre;
         if((tbIdMat.getText().isEmpty()) && tbNombreMat.getText().isEmpty())  {
           JOptionPane.showMessageDialog(null, "Ingrese codigo o nombre", "Info", JOptionPane.WARNING_MESSAGE);  
         }
         else {
          if(!tbIdMat.getText().isEmpty()){     
           
          id=Integer.parseInt(tbIdMat.getText());             
          Materia mat= md.buscarMateria(id);
          if (mat !=null){
            tbIdMat.setText(mat.getId()+"");
            tbNombreMat.setText(mat.getNombre());
            tbResponsableMat.setText( mat.getResponsable());
            tbPeriodoMat.setText( mat.getPeriodo());  
            
        } else {
         JOptionPane.showMessageDialog(null, "La materia no existe", "Info", JOptionPane.WARNING_MESSAGE); 
        }
         }
        if (!tbNombreMat.getText().isEmpty()){  
        nombre=tbNombreMat.getText();             
        Materia mat= md.buscarMateriaXNombre(nombre);
        if (mat !=null){
        tbIdMat.setText(mat.getId()+"");
            tbNombreMat.setText(mat.getNombre());
            tbResponsableMat.setText( mat.getResponsable());
            tbPeriodoMat.setText( mat.getPeriodo()); 
        } else {
         JOptionPane.showMessageDialog(null, "La materia no existe", "Info", JOptionPane.WARNING_MESSAGE); 
        } 
           // }   
           }       
        }
    }//GEN-LAST:event_btnBuscarMatActionPerformed

    private void btnModificarMatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarMatActionPerformed
        // TODO add your handling code here:}
         if((tbIdMat.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Debe ingresar un codigo de materia", "Eliminar", JOptionPane.WARNING_MESSAGE); 
        }else{ 
        int id=Integer.parseInt(tbIdMat.getText());
        nombre= tbNombreMat.getText();
        responsable=tbResponsableMat.getText();
        periodo=tbPeriodoMat.getText();
        
        Materia materia = new Materia(id,nombre,responsable,periodo);
        md.actualizarMateria(materia);
        JOptionPane.showMessageDialog(null, "Se actualizo Materia", "Info", JOptionPane.WARNING_MESSAGE); 
         }
    }//GEN-LAST:event_btnModificarMatActionPerformed

    private void btnEliminarMatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarMatActionPerformed
        // TODO add your handling code here:
        if((tbIdMat.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Debe ingresar un codigo de materia", "Eliminar", JOptionPane.WARNING_MESSAGE); 
        }else{
        int id=Integer.parseInt(tbIdMat.getText());
            try {
               boolean seElimino= md.borrarMateria(id);
               if(seElimino) JOptionPane.showMessageDialog(null, "Se elimino materia", "Info", JOptionPane.WARNING_MESSAGE); 
            } catch (SQLException ex) {
                Logger.getLogger(VistaMaterias.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
       limpiar();
    }//GEN-LAST:event_btnEliminarMatActionPerformed

